    void CargarTablaEmpleado(String valor)
    {
    String[] titulos={"Nit","Dv","Nombres Y Apellidos"}; 
    String[] registro =new String[3];    
    String sSQL="";
    modelo =new DefaultTableModel(null,titulos);    
    ConexionMySQL mysql = new ConexionMySQL();
     Connection cn=mysql.getConnection();
     
     
     


     
     
//     sSQL=" SELECT codigo,nombres,direccion,telefono,codigo_car FROM estudiantes"
//             + "WHERE codigo_car LIKE '%"+valor+"%';";
   if (valor.equals(""))
      sSQL ="select nitocc,dv,razonsocial from empresa where nitocc = nitocc ";
   else
     sSQL = "select nitocc,dv,razonsocial from empresa where razonsocial LIKE '%"+valor+"%';";
        try 
        {
         Statement st =cn.createStatement();
         ResultSet rs =st.executeQuery(sSQL);
         
         
         while(rs.next())
         {
         registro[0]= rs.getString("nitocc");
         registro[1]= rs.getString("dv");
         registro[2]= rs.getString("razonsocial");
         
         
         modelo.addRow(registro);
         jTconsultaTercero.setModel(modelo);
         
         
         
         }
         int[] anchos = {80,25,600};
         for(int i = 0; i < jTconsultaTercero.getColumnCount(); i++) {
jTconsultaTercero.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
                 
         }
         
         
        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }
    
    
    
    
    
    
    
